package practice;

public class SwappingExample {
	public static void main(String aa[]) {
        int a = 10;
        int b = 20;
        System.out.println("Value of a is :" +a);
        System.out.println("Value of b is :" +b);
        int swap = a;
        a = b;
        b = swap;
        System.out.println("Value of a is :" +a);
        System.out.println("Value of b is :" +b);
       
}
}
